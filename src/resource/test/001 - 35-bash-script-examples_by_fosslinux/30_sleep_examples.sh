#30. The Sleep Command

#The sleep command helps the user to pause an ongoing task for a specified period. It gives you the allowance of delaying or halting/pausing for hours, minutes, seconds, or days. For illustration purposes, create a file and name it ‘sleep_examples.sh’, thereafter run the script shown below.

#The example aims to delay the task for approximately 8 seconds after execution.

#!/bin/bash

echo “Please be patient for 8 seconds”
sleep 8
echo “Completed”